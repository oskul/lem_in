#include "libft.h"

int	ft_w(char const *s, char c)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != '\0')
			k++;
		while (s[i] != c && s[i])
			i++;
	}
	return (k);
}
